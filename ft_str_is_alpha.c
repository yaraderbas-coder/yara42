int	ft_str_is_alpha(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if ((str[c] >= 'a' && str[c] <= 'z')
			|| (str[c] >= 'A' && str[c] <= 'Z'))
			c++;
		else
			return (0);
	}
	return (1);
}
